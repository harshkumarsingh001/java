public class Escape_sequence {
    public static void main(String[]args){

        System.out.println("The sun shines brightly  in the clear blue sky.");

        System.out.println("The sun shines brightly \tin the clear blue sky.");

        System.out.println("The sun shines brightly \nin the clear blue sky.");

        System.out.println("The sun shines brightly \'in the clear blue sky.");

         System.out.println("The sun shines brightly \\in the clear blue sky.");


    }

    
}
