public class increment__decrement {
    public static void main(String[]args){
        int a=45;
        int b=++a;
        int c=87;
        int d=c++;
        int e=56;
        int f=--e;
        int g=77;
        int h=g--;
        System.out.println(b);
        System.out.println(d);
        System.out.println(f);
        System.out.println(h);
       int y=7;
       int x=++y*8;
       System.out.println(x);

       char j='A';
       char k=++j;
       System.out.println(k);

    }
}
